package DumbDataHolders;

public class AssignmentDumbData {

    public AssignmentDumbData(String assignmentID, String assignmentName, String classID, String dueDate, String doDate) {
        this.assignmentID = assignmentID;
        AssignmentName = assignmentName;
        ClassID = classID;
        DueDate = dueDate;
        DoDate = doDate;
    }

    public String assignmentID;
    public String AssignmentName;
    public String ClassID;
    public String DueDate;
    public String DoDate;
}
